    private void carregar() {
    new Thread(() -> { // roda o carregamento em outra thread
        for (int i = 0; i <= 100; i++) {
            try {
                Thread.sleep(50);
            } catch (Exception e) {}
            BarraProgresso.setValue(i);
        }
        
        // quando terminar o carregamento, abre a prÃ³xima tela
        TelaMedia tela = new TelaMedia(); 
        tela.setVisible(true);
        dispose(); // fecha a tela de carregamento
    }).start(); // inicia a thread
}

